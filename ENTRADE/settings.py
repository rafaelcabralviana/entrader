def _build_caches() -> dict:
    """
    LocMem: um cache por processo Gunicorn.

    Redis (recomendado em produção): defina **só** ``DJANGO_CACHE_URL`` (ex. redis://127.0.0.1:6379/1).
    Não derivamos mais automaticamente do broker do Celery — URL errada bloqueava o site ao abrir
    qualquer página (timeout TCP no ``cache.get``). ``DJANGO_USE_REDIS_CACHE=1`` continua a
    permitir derivar de ``CELERY_BROKER_URL`` (DB lógico /1) se não houver ``DJANGO_CACHE_URL``.
    """
    loc: dict = {
        'default': {
            'BACKEND': 'django.core.cache.backends.locmem.LocMemCache',
            'LOCATION': 'entrade-api-auth',
        }
    }
    if _running_django_manage_test():
        return loc
    if 'pytest' in sys.modules:
        return loc
    if _env_bool('DJANGO_DISABLE_REDIS_CACHE', False):
        return loc
    url = (os.environ.get('DJANGO_CACHE_URL') or '').strip()
    if not url and _env_bool('DJANGO_USE_REDIS_CACHE', False):
        broker = (os.environ.get('CELERY_BROKER_URL') or '').strip()
        if broker.startswith(('redis://', 'rediss://')):
            if re.search(r'/\d+\s*$', broker):
                url = re.sub(r'/\d+\s*$', '/1', broker)
            else:
                url = broker.rstrip('/') + '/1'
    if not url:
        return loc
    try:
        sock = max(1, int(os.environ.get('DJANGO_REDIS_SOCKET_TIMEOUT_SEC', '3') or '3'))
    except ValueError:
        sock = 3
    return {
        'default': {
            'BACKEND': 'django.core.cache.backends.redis.RedisCache',
            'LOCATION': url,
            'OPTIONS': {
                # redis.ConnectionPool (evita espera infinita se o host não responde).
                'socket_connect_timeout': sock,
                'socket_timeout': sock,
            },
        }
    }


def _postgres_options_from_url_query(query: str) -> dict:
    opts: dict = {}
    if not query:
        return opts
    for key, values in parse_qs(query, keep_blank_values=True).items():
        if not values:
            continue
        if key.lower() == 'sslmode' and values[0]:
            opts['sslmode'] = values[0]
    return opts


def _database_from_postgres_url(url: str) -> dict | None:
    """Aceita postgresql:// e postgres:// (Heroku / Docker)."""
    raw = (url or '').strip()
    if not raw:
        return None
    if raw.startswith('postgres://'):
        raw = 'postgresql://' + raw[len('postgres://') :]
    if not raw.startswith('postgresql://'):
        return None
    parsed = urlparse(raw)
    path = (parsed.path or '').lstrip('/')
    if not path:
        return None
    db: dict = {
        'ENGINE': 'django.db.backends.postgresql',
        'NAME': unquote(path),
        'USER': unquote(parsed.username or ''),
        'PASSWORD': unquote(parsed.password or ''),
        'HOST': parsed.hostname or 'localhost',
        'PORT': str(parsed.port or 5432),
    }
    opts = _postgres_options_from_url_query(parsed.query or '')
    extra_ssl = (os.environ.get('DJANGO_DB_SSLMODE') or '').strip()
    if extra_ssl:
        opts['sslmode'] = extra_ssl
    if opts:
        db['OPTIONS'] = opts
    return db


def _database_from_postgres_env() -> dict | None:
    """PostgreSQL via variáveis DJANGO_DB_* (sem URL)."""
    name = (os.environ.get('DJANGO_DB_NAME') or '').strip()
    user = (os.environ.get('DJANGO_DB_USER') or '').strip()
    host = (os.environ.get('DJANGO_DB_HOST') or '').strip()
    if not (name and user and host):
        return None
    db: dict = {
        'ENGINE': 'django.db.backends.postgresql',
        'NAME': name,
        'USER': user,
        'PASSWORD': os.environ.get('DJANGO_DB_PASSWORD', ''),
        'HOST': host,
        'PORT': (os.environ.get('DJANGO_DB_PORT') or '5432').strip() or '5432',
    }
    sslmode = (os.environ.get('DJANGO_DB_SSLMODE') or '').strip()
    if sslmode:
        db['OPTIONS'] = {'sslmode': sslmode}
    return db


