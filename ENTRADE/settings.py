def _sqlite_busy_timeout_sec() -> int:
    try:
        # Replay + perfil paralelo + Celery: espera longa no busy handler reduz OperationalError.
        return max(5, int(os.environ.get('DJANGO_SQLITE_BUSY_TIMEOUT_SEC', '90') or '90'))
    except ValueError:
        return 90


def _build_databases() -> dict:
    """
    PostgreSQL se DJANGO_DATABASE_URL / DATABASE_URL ou DJANGO_DB_NAME+USER+HOST.
    Caso contrário SQLite (db.sqlite3) para desenvolvimento sem Postgres.
    """
    for key in ('DJANGO_DATABASE_URL', 'DATABASE_URL'):
        cfg = _database_from_postgres_url(os.environ.get(key, ''))
        if cfg is not None:
            cfg['CONN_MAX_AGE'] = _conn_max_age()
            return {'default': cfg}
    cfg = _database_from_postgres_env()
    if cfg is not None:
        cfg['CONN_MAX_AGE'] = _conn_max_age()
        return {'default': cfg}
    return {
        'default': {
            'ENGINE': 'django.db.backends.sqlite3',
            'NAME': BASE_DIR / 'db.sqlite3',
            # Segundos à espera de lock (vários workers + Celery = sem isto pode “congelar” o site).
            'OPTIONS': {'timeout': _sqlite_busy_timeout_sec()},
        }
    }


